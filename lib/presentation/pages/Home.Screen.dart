import 'package:e_commerce_shoes/core/Theme/appcolors.dart';
import 'package:e_commerce_shoes/presentation/pages/cart.dart';
import 'package:e_commerce_shoes/presentation/pages/favourite.dart';
import 'package:e_commerce_shoes/presentation/pages/home.dart';
import 'package:e_commerce_shoes/presentation/pages/profile.dart';
import 'package:flutter/material.dart';

class HomeBottomnavigation extends StatefulWidget {
   const HomeBottomnavigation({super.key});

  @override
  State<HomeBottomnavigation> createState() => _HomeBottomnavigationState();
}

class _HomeBottomnavigationState extends State<HomeBottomnavigation> {
int _currentIndex = 0;

final List<Widget> pages = [

    const Home(),
    const Cart(),
    const Favourite(),
     Profile(),
  ];

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      body: pages.elementAt(_currentIndex),
       bottomNavigationBar: BottomNavigationBar(
         selectedItemColor:   AppColors.primarycolor,
  unselectedItemColor:AppColors.kgrey,
        items: const [
         
        BottomNavigationBarItem(icon: Icon(Icons.home),
        label: 'Home'),
          BottomNavigationBarItem(icon: Icon(Icons.shopping_cart),
        label: 'Cart'),
          BottomNavigationBarItem(icon: Icon(Icons.favorite_border),
        label: 'Favorite'),
          BottomNavigationBarItem(icon: Icon(Icons.person),
        label: 'Profile'),

       ],
       currentIndex: _currentIndex,
       onTap: (value) {
         setState(() {
          _currentIndex = value;
         });
       },
       ),
    ) ;
  }
}